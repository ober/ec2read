class Ec2read

  def self.readit(file)
    File.open(file,"r").read
    #puts "what is foo? #{foo.class} #{foo.split.count}"
  end

  def self.gimmehash(contents)
    # Reservation
    # Instance
    # Blockdevice
    # Tag
    if contents.nil?
      puts "WTF: got nil arg in #{__method__} contents:#{contents.class}"
      exit 2
    end
    contents.inject({}) { |results, line|

      puts case line.split("\t").first
        when "RESERVATION" then "reservation"
        when "INSTANCE" then "instance"
        when "BLOCKDEVICE" then"blockdevice"
        when "TAG" then "tag"
           else
             "wtf:#{line.split("\t").first}"
      end
    }
  end

  def self.doit(file)
    gimmehash(readit(file))
  end
end

